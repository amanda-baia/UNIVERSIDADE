#b) Desenvolva um algoritmo que receba 5 números , calcule a média aritmética e apresente o resultado final:

a = float(input('digite o valor do primeiro numero'))
b = float(input('digite o valor do segundo numero'))
c = float(input('digite o valor do terceiro numero'))
d = float(input('digite o valor do quarto numero'))
e = float(input('digite o valor do quinto numero'))

M= (a*b*c*d*e)/5
print ("O resultado da média aritmética é:", M)
